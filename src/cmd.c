#include "push_swap.h"

void
	do_cmd(char *cmd, t_stack **a, t_stack **b)
{
	if (ft_strcmp(cmd, "sa") == 0)
		swap_stack(a);
	else if (ft_strcmp(cmd, "sb") == 0)
		swap_stack(b);
}
